function mostrarOcultarParrafo() {
    var parrafoSeguridad = document.getElementById("parrafoSeguridad");

    // Verifica si el párrafo está visible
    if (parrafoSeguridad.style.display === "none" || parrafoSeguridad.style.display === "") {
        // Si está oculto, lo muestra
        parrafoSeguridad.style.display = "block";
    } else {
        // Si está visible, lo oculta
        parrafoSeguridad.style.display = "none";
    }
}
